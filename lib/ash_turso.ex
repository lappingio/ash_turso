defmodule AshTurso do
  @moduledoc """
  The AshTurso extension gives you tools to map a resource to a Turso/libSQL database table.

  AshTurso is built on top of `ecto_libsql`, which provides native connectivity to:

  - **Local SQLite** databases (for development)
  - **Turso Cloud** databases (for production)
  - **Embedded replicas** (local SQLite synced with Turso Cloud)

  For more, check out the [getting started guide](/documentation/tutorials/getting-started-with-ash-turso.md)
  """
end
